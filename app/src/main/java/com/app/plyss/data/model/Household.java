package com.app.plyss.data.model;

public class Household {
    private String agentId;
    private String father_name;
    private String mother_name;
    private String address;
    private String street;
    private String marital_status;
    private String occupation;
    private String state_of_origin;
    private String lga_of_origin;
    private String number_of_wives;
    private int children_number;
    private int dependants_number;
    private String date_of_capture;
    private String time_of_capture;
    private String image;
    private String residence;

    public Household() {
    }

    public String getAgentId() {
        return agentId;
    }

    public void setAgentId(String agentId) {
        this.agentId = agentId;
    }

    public String getFather_name() {
        return father_name;
    }

    public void setFather_name(String father_name) {
        this.father_name = father_name;
    }

    public String getMother_name() {
        return mother_name;
    }

    public void setMother_name(String mother_name) {
        this.mother_name = mother_name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getMarital_status() {
        return marital_status;
    }

    public void setMarital_status(String marital_status) {
        this.marital_status = marital_status;
    }

    public String getOccupation() {
        return occupation;
    }

    public void setOccupation(String occupation) {
        this.occupation = occupation;
    }

    public String getState_of_origin() {
        return state_of_origin;
    }

    public void setState_of_origin(String state_of_origin) {
        this.state_of_origin = state_of_origin;
    }

    public String getLga_of_origin() {
        return lga_of_origin;
    }

    public void setLga_of_origin(String lga_of_origin) {
        this.lga_of_origin = lga_of_origin;
    }

    public String getNumber_of_wives() {
        return number_of_wives;
    }

    public void setNumber_of_wives(String number_of_wives) {
        this.number_of_wives = number_of_wives;
    }

    public int getChildren_number() {
        return children_number;
    }

    public void setChildren_number(int children_number) {
        this.children_number = children_number;
    }

    public int getDependants_number() {
        return dependants_number;
    }

    public void setDependants_number(int dependants_number) {
        this.dependants_number = dependants_number;
    }

    public String getDate_of_capture() {
        return date_of_capture;
    }

    public void setDate_of_capture(String date_of_capture) {
        this.date_of_capture = date_of_capture;
    }

    public String getTime_of_capture() {
        return time_of_capture;
    }

    public void setTime_of_capture(String time_of_capture) {
        this.time_of_capture = time_of_capture;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getResidence() {
        return residence;
    }

    public void setResidence(String residence) {
        this.residence = residence;
    }
}
